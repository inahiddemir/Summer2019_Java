package day19_Forloop;

public class FotLoop {
	public static void main(String[] args) {
		
// initializer  / condition / iterator
		for(int i = 1; i<=10;) {
			System.out.println("Hello World");
		}
	}
}
// if you dont put any iterator, thats ok. You dint get any compile error but for loop doesnt stop.
 
/*
		initialization: starting point   int i = 10 .............
		condition: created based on the initialization i>0 .............
		iterator: co related the condition.
		
		Iterator needs to be capable enough to make the condition false
		
		if the condition loop is true, the loop will be keep executed.



*/