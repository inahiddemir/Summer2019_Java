package day06_ArithmaticOperators;

public class ArithmaticOperators {

	/*
	 + : addition, add two numbers together
	 - : subtraction
	 /: division
	  * *: multiplication
	  
	 */
	public static void main(String[] args) {
		
		int a = 100;
		int b = a-400;
		System.out.println(b);
		
		int c = a+b;
		System.out.println(c);
		int X =3, Y=5, Z=X*Y;
		System.out.println(Z);
		
		double length = 100, width=10, area = length*width;
		
		System.out.println(area);
		System.out.println(3+5+4-2);
	
		double Number1 = 10/3.0;
		
		System.out.println(Number1);
		int output = 12+6/3;
		System.out.println(output);
		
		
		// % :
		
		/*
		 	10/3 ==> 3 with remainder of 1
		 	12/5 ==> 2 with remainder of 2
		 */
	
		int result1 = 10%3;
		System.out.println(result1);
		System.out.println(12%5);
		
		int Number20 = 20%2;
		System.out.println(Number20);
		int Number45 = 45%2;
		System.out.println(Number45);
		
		
		
		
		
		
		
		
	}
	
	
	
	
	
}
